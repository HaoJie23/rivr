package com.nuecho.rivr.core.dialogue;

/**
 * @author Nu Echo Inc.
 */
public class DialogueFactoryException extends Exception {

    private static final long serialVersionUID = 1L;

    public DialogueFactoryException() {
        super();
    }

    public DialogueFactoryException(String message, Throwable cause) {
        super(message, cause);
    }

    public DialogueFactoryException(String message) {
        super(message);
    }

    public DialogueFactoryException(Throwable cause) {
        super(cause);
    }
}
